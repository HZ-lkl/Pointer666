#include<stdio.h>

int main()
{
	/*二维数组
		利用指针遍历
	
	*/

//1.定义一个数组
	int arr[3][5] =
	{
		{1,2,3,4,5},
		{11,22,33,44,55},
		{111,222,333,444,555}
	};

	//2.利用指针遍历二维数组
	//获取二维数组的指针

	//指针类型和内部元素类型保存一致

	int(*p)[5] = arr;

	/*printf("%p\n", arr);
	printf("%p\n", arr + 1);*/

	for (int i = 0; i < 3; i++)
	{
		for (int j = 0; j < 5; j++)
		{
			printf("%d ", *(*p + j));//；两次解开
		}
		printf("\n");
		//移动二维数组的指针，继续遍历下一个一维数组
		p++;
	}
}